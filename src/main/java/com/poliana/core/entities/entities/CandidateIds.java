package com.poliana.core.entities.entities;

public class CandidateIds {
    private String recipientExtId;
    private String crpName;
    private String party;
    private String distIdRunFor;
    private String fecCandId;

    public String getRecipientExtId() {
        return recipientExtId;
    }

    public void setRecipientExtId(String recipientExtId) {
        this.recipientExtId = recipientExtId;
    }

    public String getCrpName() {
        return crpName;
    }

    public void setCrpName(String crpName) {
        this.crpName = crpName;
    }

    public String getParty() {
        return party;
    }

    public void setParty(String party) {
        this.party = party;
    }

    public String getDistIdRunFor() {
        return distIdRunFor;
    }

    public void setDistIdRunFor(String distIdRunFor) {
        this.distIdRunFor = distIdRunFor;
    }

    public String getFecCandId() {
        return fecCandId;
    }

    public void setFecCandId(String fecCandId) {
        this.fecCandId = fecCandId;
    }
}
