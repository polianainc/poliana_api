package com.poliana.campaignFinance.entities;

public class PacToCandidateContribution {
    private String cycle;
    private String fecRecNo;
    private String pacId;
    private String cid;
    private double amount;
    private String date;
    private String realCode;
    private String type;
    private String di;
    private String fecCandId;

    public String getCycle() {
        return cycle;
    }

    public void setCycle(String cycle) {
        this.cycle = cycle;
    }

    public String getFecRecNo() {
        return fecRecNo;
    }

    public void setFecRecNo(String fecRecNo) {
        this.fecRecNo = fecRecNo;
    }

    public String getPacId() {
        return pacId;
    }

    public void setPacId(String pacId) {
        this.pacId = pacId;
    }

    public String getCid() {
        return cid;
    }

    public void setCid(String cid) {
        this.cid = cid;
    }

    public double getAmount() {
        return amount;
    }

    public void setAmount(double amount) {
        this.amount = amount;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getRealCode() {
        return realCode;
    }

    public void setRealCode(String realCode) {
        this.realCode = realCode;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getDi() {
        return di;
    }

    public void setDi(String di) {
        this.di = di;
    }

    public String getFecCandId() {
        return fecCandId;
    }

    public void setFecCandId(String fecCandId) {
        this.fecCandId = fecCandId;
    }
}
